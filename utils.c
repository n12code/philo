#include "philo.h"

long	ft_atol(char *str)
{
	size_t	i;
	long	nb;

	i = 0;
	nb = 0;
	while (str[i])
	{
		if (nb > (__LONG_MAX__ - (str[i] - 48)) / 10 )
			return (-1);
		nb *= 10;
		nb += str[i] - 48;
		i++;
	}
	return (nb);
}
