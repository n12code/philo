#include "philo.h"

u_int64_t	ato_uint(char *str)
{
	size_t	i;
	u_int64_t	nb;

	i = 0;
	nb = 0;
	while (str[i])
	{
		nb *= 10;
		nb += str[i] - 48;
		i++;
	}
	return (nb);
}